/**
 * root - helper fctn
 * @n: number
 * Return: root
 */

int root(int n, itn i)
{
	if (n == (i * i))
		return (i);
}

/**
 * _sqrt_recursion - function to determine the sqrt
 * @n: number
 * Return: returns sqrt of a number
 */

int _sqrt_recursion(int n)
{
	if (n < 0)
		return (-1);
	return (root(n, 1));
}
